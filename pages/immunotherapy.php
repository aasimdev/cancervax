                        <p class="text-base leading-relaxed text-black">These are immune system proteins created in the lab that are designed to bind to specific targets on cancer cells. Some monoclonal antibodies mark cancer cells so that they will be better seen and destroyed by the immune system. Such monoclonal antibodies are a type of immunotherapy.</p>
