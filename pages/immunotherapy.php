                    <p class="text-black text-base leading-relaxed">For decades, surgery, chemotherapy, and radiation therapy have been the standards of care for cancer treatment and are often the only options. They operate on the basic premise that cancer cells are fast-growing cells, so using powerful toxic chemicals (chemotherapy) or powerful X-rays (radiation), they hope to kill those cells to stop the cancer. The problem is that these treatments are not always very targeted, so they also kill normal fast-growing cells, such as hair cells, intestine cells, mouth-lining cells, and others. This inadvertent side effect of killing healthy cells is what causes the debilitating side effects of chemotherapy and radiation and a reduced quality of life for cancer patients. <br><br>

                        Using “chemo” has been likened to throwing a grenade into the body with the hope that it will kill the cancer and not the body.</p>
