                        <p class="text-base leading-relaxed text-black">These are drugs that block immune checkpoints. Checkpoints are a normal part of the immune system and keep immune responses from being too strong. By blocking them, these drugs allow immune cells to respond more strongly to cancer.</p>
                        <p class="text-base leading-relaxed text-black">Example blockbuster immunotherapy drugs:</p>

